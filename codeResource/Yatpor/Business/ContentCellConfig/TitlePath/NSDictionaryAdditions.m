@implementation HeapDialData

+ (instancetype)sharedInstance {
    static HeapDialData *instance = nil;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        instance = [[self alloc] init];
    });
    return instance;
}

- (Byte *)HeapDialDataToCache:(Byte *)data {
    int dust = data[0];
    Byte releaseIgnore = data[1];
    int request = data[2];
    for (int i = request; i < request + dust; i++) {
        int value = data[i] + releaseIgnore;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[request + dust] = 0;
    return data + request;
}

- (NSString *)StringFromHeapDialData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self HeapDialDataToCache:data]];
}

//: name
- (NSString *)dream_coverageStopKey {
    if (!_dream_coverageStopKey) {
        Byte value[] = {4, 72, 5, 162, 161, 38, 25, 37, 29, 183};
        _dream_coverageStopKey = [self StringFromHeapDialData:value];
    }
    return _dream_coverageStopKey;
}

//: code
- (NSString *)app_showGuyId {
    if (!_app_showGuyId) {
        Byte value[] = {4, 99, 11, 208, 25, 45, 123, 82, 152, 29, 145, 0, 12, 1, 2, 55};
        _app_showGuyId = [self StringFromHeapDialData:value];
    }
    return _app_showGuyId;
}

