Byte * PartDataToCache(Byte *data) {
    int blaze = data[0];
    int professional = data[1];
    Byte actRegion = data[2];
    int comparableFellow = data[3];
    if (!blaze) return data + comparableFellow;
    for (int i = comparableFellow; i < comparableFellow + professional; i++) {
        int value = data[i] - actRegion;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[comparableFellow + professional] = 0;
    return data + comparableFellow;
}

NSString *StringFromPartData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PartDataToCache(data)];
}
