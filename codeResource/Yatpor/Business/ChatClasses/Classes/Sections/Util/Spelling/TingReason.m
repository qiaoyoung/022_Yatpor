+ (NSData *)TacticDataToData:(NSString *)value {
    NSMutableArray<NSNumber *> *array = [NSMutableArray array];
    for (NSUInteger i = 0; i < value.length; i += 2) {
        NSString *hex = [value substringWithRange:NSMakeRange(i, 2)];
        NSScanner *scanner = [NSScanner scannerWithString:hex];
        unsigned int num;
        if ([scanner scanHexInt:&num]) {
            [array addObject:@(num)];
        }
    }

    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] intValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}

+ (Byte *)TacticDataToByte:(StructTacticData *)data {
    for (int i = 0; i < data->dismissNeed; i++) {
        data->nighResort[i] ^= data->layerGo;
    }
    data->nighResort[data->dismissNeed] = 0;
	if (data->dismissNeed >= 1) {
		data->othersAct = data->nighResort[0];
	}
    return data->nighResort;
}

+ (NSString *)StringFromTacticData:(StructTacticData *)data {
    return [NSString stringWithUTF8String:(char *)[self TacticDataToByte:data]];
}

