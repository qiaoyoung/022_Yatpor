+ (NSData *)BubbleDataToData:(NSString *)value {
    NSMutableArray<NSNumber *> *array = [NSMutableArray array];
    for (NSUInteger i = 0; i < value.length; i += 2) {
        NSString *hex = [value substringWithRange:NSMakeRange(i, 2)];
        NSScanner *scanner = [NSScanner scannerWithString:hex];
        unsigned int num;
        if ([scanner scanHexInt:&num]) {
            [array addObject:@(num)];
        }
    }

    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] intValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}

+ (Byte *)BubbleDataToCache:(Byte *)data {
    int bubbleGray = data[0];
    Byte dataModel = data[1];
    int heritage = data[2];
    for (int i = heritage; i < heritage + bubbleGray; i++) {
        int value = data[i] + dataModel;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[heritage + bubbleGray] = 0;
    return data + heritage;
}

+ (NSString *)StringFromBubbleData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self BubbleDataToCache:data]];
}

