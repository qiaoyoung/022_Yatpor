Byte * SymptomDataToCache(Byte *data) {
    int equip = data[0];
    int average = data[1];
    Byte valueSingle = data[2];
    int usWithForty = data[3];
    if (!equip) return data + usWithForty;
    for (int i = usWithForty; i < usWithForty + average; i++) {
        int value = data[i] + valueSingle;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[usWithForty + average] = 0;
    return data + usWithForty;
}

NSString *StringFromSymptomData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SymptomDataToCache(data)];
}
