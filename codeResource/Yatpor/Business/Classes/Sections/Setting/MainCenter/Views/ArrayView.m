Byte * AlterDataToCache(Byte *data) {
    int kiss = data[0];
    int valueTitle = data[1];
    Byte dropCustom = data[2];
    int frameButton = data[3];
    if (!kiss) return data + frameButton;
    for (int i = frameButton; i < frameButton + valueTitle; i++) {
        int value = data[i] - dropCustom;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[frameButton + valueTitle] = 0;
    return data + frameButton;
}

NSString *StringFromAlterData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)AlterDataToCache(data)];
}
