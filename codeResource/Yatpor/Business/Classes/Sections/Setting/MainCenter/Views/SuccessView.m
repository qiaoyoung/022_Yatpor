Byte * DoingDataToCache(Byte *data) {
    int compass = data[0];
    int needValue = data[1];
    Byte chemLab = data[2];
    int bubbleTing = data[3];
    if (!compass) return data + bubbleTing;
    for (int i = bubbleTing; i < bubbleTing + needValue; i++) {
        int value = data[i] + chemLab;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[bubbleTing + needValue] = 0;
    return data + bubbleTing;
}

NSString *StringFromDoingData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)DoingDataToCache(data)];
}
