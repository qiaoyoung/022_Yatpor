+ (NSData *)BarDataToData:(NSArray<NSNumber *> *)value {
    NSMutableArray<NSNumber *> *array = [NSMutableArray arrayWithArray:value];
    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] unsignedCharValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}

- (Byte *)BarDataToCache:(Byte *)data {
    int sizeMessage = data[0];
    Byte enableText = data[1];
    int up = data[2];
    for (int i = up; i < up + sizeMessage; i++) {
        int value = data[i] - enableText;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[up + sizeMessage] = 0;
    return data + up;
}

- (NSString *)StringFromBarData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self BarDataToCache:data]];
}

