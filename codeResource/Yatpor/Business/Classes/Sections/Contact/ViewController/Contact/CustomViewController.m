+ (NSData *)CommentDataToData:(NSArray<NSString *> *)value {
    NSMutableArray<NSString *> *array = [NSMutableArray arrayWithArray:value];
    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] intValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}

- (Byte *)CommentDataToByte:(StructCommentData *)data {
    for (int i = 0; i < data->ting; i++) {
        data->operation[i] ^= data->endOpen;
    }
    data->operation[data->ting] = 0;
	if (data->ting >= 2) {
		data->enable = data->operation[0];
		data->dropBubble = data->operation[1];
	}
    return data->operation;
}

- (NSString *)StringFromCommentData:(StructCommentData *)data {
    return [NSString stringWithUTF8String:(char *)[self CommentDataToByte:data]];
}

